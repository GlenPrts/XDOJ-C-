#include <stdio.h>
int main()
{
    int n, m, s = 0;
    scanf("%d", &n);
    m = n;
    if (n > 1000000)
    {
        m -= 1000000;
        s += m * 0.01;
        m = 1000000;
    }
    if (n >600000)
    {
        m -= 600000;
        s += m * 0.015;
        m = 600000;
    }
    if (n > 400000)
    {
        m -= 400000;
        s += m * 0.03;
        m = 400000;
    }
    if (n > 200000)
    {
        m -= 200000;
        s += m * 0.05;
        m = 200000;
    }
    if (n > 100000)
    {
        m -= 100000;
        s += m * 0.075;
        m = 100000;
    }
    if (n > 0)
    {
        s += m * 0.1;
    }
    printf("%d", s);
    return 0;
}