#include <stdio.h>
int main()
{
    float n, m = 0, s;
    scanf("%f", &n);
    s = n;
    if (n > 1000000)
    {
        s -= 1000000;
        m += s * 0.01;
        s = 1000000;
    }
    if (n > 600000)
    {
        s -= 600000;
        m += s * 0.015;
        s = 600000;
    }
    if (n > 400000)
    {
        s -= 400000;
        m += s * 0.03;
        s = 400000;
    }
    if (n > 200000)
    {
        s -= 200000;
        m += s * 0.05;
        s = 200000;
    }
    if (n > 100000)
    {
        s -= 100000;
        m += s * 0.075;
        s = 100000;
    }
    if (n > 0)
    {
        m += s * 0.1;
    }
    printf("%.2f", m);
    return 0;
}